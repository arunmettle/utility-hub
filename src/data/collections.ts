const missingToolIds = personaCollections.flatMap((collection) =>
  collection.toolIds.filter((toolId) => !toolLookup.has(toolId)),
);

if (missingToolIds.length > 0) {
  throw new Error(`Unknown tool ids in persona collections: ${missingToolIds.join(', ')}`);
}

export const personaCollectionBySlug = new Map(personaCollections.map((collection) => [collection.slug, collection]));

export function getPersonaCollectionBySlug(slug: string) {
  return personaCollectionBySlug.get(slug);
}

export function getPersonaCollectionTools(collection: PersonaCollectionDefinition): ToolDefinition[] {
  return collection.toolIds
    .map((toolId) => toolLookup.get(toolId))
    .filter((tool): tool is ToolDefinition => Boolean(tool));
}

export const featuredPersonaCollections = featuredPersonaCollectionSlugs
  .map((slug) => personaCollectionBySlug.get(slug))
  .filter((collection): collection is PersonaCollectionDefinition => Boolean(collection));
